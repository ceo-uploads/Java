class ifElseCondition {
  public static void main(String[] args) {
    // instances..
    int time = 22;
    // branh-- if/else condition...
    if (time < 10) {
       System.out.println("Good morning.");
    } else if (time < 18) {
       System.out.println("Good day.");
    } else {
       System.out.println("Good evening.");
    }
    // Outputs "Good evening."
  }
}
